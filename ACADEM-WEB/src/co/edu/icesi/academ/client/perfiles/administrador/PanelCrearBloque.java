	public void crearBloque() {
		if(textNombreBloque.getValue()!=null && textNombreBloque.getValue()!="") {
			BloqueBO bloqueBO = new BloqueBO();
			bloqueBO.setNombre(textNombreBloque.getValue());
			ControladorAdministrador.getInstance().crearBloque(bloqueBO);
			ControladorAdministrador.getInstance().getAcademUI().cerrarVentanaEmergente();
		}else
			ControladorAdministrador.getInstance().getAcademUI().mostrarNotificacion("cuidado", "Debe ingresar un nombre para bloque");
	}

